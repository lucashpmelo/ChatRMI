    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String senha1 = new String(txtSenha1.getPassword());
        String senha2 = new String(txtSenha2.getPassword());
        
        if(!txtLogin.getText().equals("") && !txtEmail.getText().equals("") && !senha1.equals("") && !senha2.equals("")){
            if(senha1.equals(senha2)){
            
                try {
                    MessageDigest m = MessageDigest.getInstance("MD5");
                    m.update(senha1.getBytes(), 0, senha1.length());

                    boolean flag = o.cadastroCliente(txtLogin.getText(), new BigInteger(1, m.digest()).toString(16), txtEmail.getText());

                    if(flag){
                        JOptionPane.showMessageDialog(null, "Usuario cadastrado com sucesso!!!");
                        dispose();
                    }else{
                        JOptionPane.showMessageDialog(null, "Usuario ou Email ja est√£o sendo usado!!!");
                    }

                } catch (NoSuchAlgorithmException ex) {
                    Logger.getLogger(JFCadastro.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(JFCadastro.class.getName()).log(Level.SEVERE, null, ex);
                }            

            }else{
                JOptionPane.showMessageDialog(null, "As senhas devem ser iguais!!!");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!!!");
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

