    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        boolean flag = false;
        String senha = new String(jtfSenha.getPassword());
        
        try {
            MessageDigest m = MessageDigest.getInstance("MD5");
            
            m.update(senha.getBytes(), 0, senha.length());
            
            flag = o.verificaLogin(jtfLogin.getText(), new BigInteger(1, m.digest()).toString(16));
        } catch (IOException ex) {
            Logger.getLogger(JFServidor.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(JFLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(flag){            
            JFClientes jCli = new JFClientes();
            jCli.usuario = jtfLogin.getText();
            try {
                jCli.atualiza();
                jCli.addUsuarios();
            } catch (RemoteException ex) {
                Logger.getLogger(JFLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            jCli.setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Login ou Senha incorretos!!!");
        }        
    }//GEN-LAST:event_btnEntrarActionPerformed

